package org.potados.base.extension

import timber.log.Timber

inline fun <T> T?.onNull(block: () ->Unit): T? {
    if (this == null) {
        block()
    }
    return this
}

fun <T> tryOrNull(logOnError: Boolean = true, body: () -> T?): T? {
    return try {
        body()
    } catch (e: Exception) {
        if (logOnError) {
            Timber.e(e)
        }

        null
    }
}

inline fun <T, R> withNonNull(receiver: T?, block: T.() -> R): R? {
    return if (receiver == null) null
    else with(receiver, block)
}
